import math 
x = 3.7 
y = 4 

print(f'Pierwiastek kwadratowy z {x} wynosi {math.sqrt(x)}')
print(f'{x} do potegi {y} wynosi {math.pow(x,y)}')
print(f'Pierwiastek stopnia {y} z {x} jest rowny {math.pow(x,1/y)}')
print(f'Silnia {y} wynosi {math.factorial(y)}')
print(f'Najwieksza mozliwa liczba calkowita mniejsza lub rowna {x} wynosi {math.floor(x)}')